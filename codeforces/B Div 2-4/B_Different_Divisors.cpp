bool isPrime(int n)
{
    if (n <= 1)
        return false;
    if (n <= 3)
        return true;

    if (n % 2 == 0 || n % 3 == 0)
        return false;

    for (int i = 5; i * i <= n; i = i + 6)
        if (n % i == 0 || n % (i + 2) == 0)
            return false;
    return true;
}
void findNextPrime(ll  left, ll & val) {
    for (ll i = left; i <= 1e8; i++) {
        if (isPrime(i)) {
            val = i;
            break;
        }
    }
}

void solve()
{
    ll d; cin >> d;
    ll x, y;
    findNextPrime(d + 1, x);
    findNextPrime(x + d, y);
    cout << x * y << endl;
}

