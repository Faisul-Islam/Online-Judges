void solve()
{
  ll n, M, H; cin >>n >> H >> M;
  ll aH = 0, aM = 0;
  ll btim = (H * 60) + M;
  ll ans = INT_MAX;
  bool zz = false;
  while(n--){
    ll h, m; cin >> h >> m;
    ll ttim = 0;
    if(H ==h && M ==m){
        zz = true;
    }
    if((h*60 + m)>btim){
       ttim = (h*60 + m) - btim;
    }else{
        ttim = (h*60 + m)  + (24 * 60 - btim);
    }
    ans = min(ans, ttim);
  }
  if(zz) cout << "0 0" << endl;
  else cout << ans/60 << " " << ans % 60 << endl;
}

